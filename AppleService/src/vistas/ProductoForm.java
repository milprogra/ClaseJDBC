    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (fila!= -1) {
            int sel= JOptionPane.showConfirmDialog(rootPane, "Â¿Seguro que quieres eliminar este registro?","Advertencia",JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (sel==0) {
                tm= (DefaultTableModel) tblProductos.getModel();
                int id = Integer.parseInt(tm.getValueAt(fila, 0).toString());
                try {
                    producto.eliminarProducto(id);
                    JOptionPane.showMessageDialog(rootPane, "Eliminado con exito!","Eliminado",JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, "Error al eliminar!","Error",JOptionPane.ERROR_MESSAGE);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Cancelado","Advertencia",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Fila no seleccionada!","Error",JOptionPane.ERROR_MESSAGE);
        }
        limpiar();
        cargaTablaInicial();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        if (fila!= -1) {
            tm = (DefaultTableModel) tblProductos.getModel();
            ProductoModificar formProductoModificar = new ProductoModificar(Integer.parseInt(tm.getValueAt(fila, 0).toString()));
            Principal.PanelPrincipal.add(formProductoModificar);
            formProductoModificar.toFront();
            formProductoModificar.show();
        }else{
            JOptionPane.showMessageDialog(rootPane, "Fila no seleccionada!","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificarActionPerformed
    
    private void cargarComboBox(){
        
        ResultSet rs = categoria.listarCategoria();
        try {
            while (rs.next()) {
                cmbCategoria.addItem(new ComboBox(rs.getInt("id"),rs.getString("nombre")));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    private void cargaTablaInicial(){
        tm = (DefaultTableModel) tblProductos.getModel();
        ResultSet rs = producto.listarProductoCategoria();
        cargarTabla(rs);
    }
    private void cargarTabla(ResultSet rs){
        
        try {
            while (rs.next()) {
                tm.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("modelo"),
                rs.getString("procesador"),
                rs.getInt("ram"),
                rs.getInt("disco"),
                rs.getInt("precio"),
                rs.getString("nombre")
                });
            }
        } catch (SQLException ex) {
            Logger.getLogger(ProductoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        tblProductos.setModel(tm);
    }
    
    private void limpiar(){
        txtModelo.setText(null);
        cmbCategoria.setSelectedIndex(0);
        fila=-1;
        limpiarTabla();
    }
    
    private void limpiarTabla(){
        tm = (DefaultTableModel) tblProductos.getModel();
        int total = tblProductos.getRowCount();
        for (int i =total-1; i >=0 ; i--) {
            tm.removeRow(i);
        }
        tblProductos.setModel(tm);
    }

