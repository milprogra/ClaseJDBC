    public void limpiar(){
        txtIdentificador.setText("");
        txtNombre.setText("");
        txtEdad.setText("");
        cmbRaza.setSelectedIndex(-1);
        chbOperado.setSelected(false);
        chbChip.setSelected(false);
    }
    
    public String comprobarSexo(){
        if (rdbMacho.isSelected()) {
            return "Macho";
        }else{
            return "hembra";
        }
    }
    
    public void instanciarSexo(String sexo){
        switch (sexo) {
            case "Macho":
                rdbMacho.setSelected(true);
            break;
            case "Hembra":
                rdbHembra.setSelected(true);
            break;
        }
    }
    
    public void cargarTabla(){
        ResultSet rs = perro.listarPerros();
        tm = (DefaultTableModel) tblPerritos.getModel();
        try {
            while (rs.next()) {
                int id = rs.getInt("id");
                String nombre = rs.getString("nombre");
                int edad = rs.getInt("edad");
                String raza = rs.getString("raza");
                String sexo= rs.getString("sexo");
                boolean operado = rs.getBoolean("operado");
                boolean chip = rs.getBoolean("chip");
                
                tm.addRow(new Object[]{id,nombre,edad,raza,sexo,operado,chip});
            }
            tblPerritos.setModel(tm);
        } catch (SQLException ex) {
            Logger.getLogger(Tablas.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

